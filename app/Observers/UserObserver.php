     * Handle the user "deleted" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function deleted(User $user)
    {

        $owned_orgs = $user->owned_orgs;
        foreach($owned_orgs as $org){
            $org->delete();
        }

    }


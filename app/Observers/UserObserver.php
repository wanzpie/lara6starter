class UserObserver
{
    /**
     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        if ($user->google_id != null)
        {
            $user->email_verified_at = Carbon::now();
            $user->save();
        }

        $user->fresh_token();
    }

